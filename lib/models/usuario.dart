

class Usuario{
  String nome = " ";
  String email = " ";
  String telefone = " ";
  String endereco = " ";
  double genero = 0;
  String senha = " ";

  Usuario(nome,email,telefone,genero,senha,endereco){
    this.nome = nome;
    this.email = email;
    this.telefone = telefone;
    this.genero = genero;
    this.senha = senha;
    this.endereco = endereco;
  }

}